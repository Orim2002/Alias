/** Fisher-Yates shuffle — returns a new shuffled array */
export function shuffleDeck(words: string[]): string[] {
  const deck = [...words];
  for (let i = deck.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [deck[i], deck[j]] = [deck[j]!, deck[i]!];
  }
  return deck;
}
